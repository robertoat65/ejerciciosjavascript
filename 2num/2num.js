let num1 = prompt("INDRODUCE TU PRIMER NUMERO")
let num2 = prompt("INTRODUCE TU SEGUNDO NUMERO")

if (num1 === num2){
    alert("Tus numeros son iguales")
}
else if (num1 > num2){
    alert("Numero 1 es mayor a numero 2")
}

else if (num1 < num2){
    alert("Numero 2 es mayor a numero 1")
}

else {
    alert("INTRODUCE NUMEROS")
}